package subsystems.individuos;

public class seccion {
    int ciMaestro;
    String ceEstudiante;
    String nivel;
    String seccion;
    boolean turno;

    public int getCiMaestro() {
        return ciMaestro;
    }

    public void setCiMaestro(int ciMaestro) {
        this.ciMaestro = ciMaestro;
    }

    public String getCeEstudiante() {
        return ceEstudiante;
    }

    public void setCeEstudiante(String ceEstudiante) {
        this.ceEstudiante = ceEstudiante;
    }

    public String getNivel() {
        return nivel;
    }

    public void setNivel(String nivel) {
        this.nivel = nivel;
    }

    public String getSeccion() {
        return seccion;
    }

    public void setSeccion(String seccion) {
        this.seccion = seccion;
    }

    public boolean isTurno() {
        return turno;
    }

    public void setTurno(boolean turno) {
        this.turno = turno;
    }

}
